public class QuickSort {
    int[] a;
    void swap(int i , int j, int[] a){
        int temp = a[i];
        a[i] = a[j];
        a[j] = temp;
    }

    void print(){
        for (int i = 0; i < i; i++) {
            System.out.println(a[i]);
        }
    }
}
